// ─── Mood 智能分配 ─────────────────────────────────────────
// 根据文本内容覆盖池子默认 mood
function _assignMood(text, defaultMood) {
  // 温暖、亲近
  if (/暖|甜|开心|一起|陪|给你|等你|留给|偏爱|治愈|被[接哄安照]|幸福|出锅|冒热气|趁热|好呀|好哦|在呢|记得|值得|愿意|真好|当然|安心|会来/.test(text)) return 'warm';
  // 冷淡、疏离
  if (/[冰]|空[了的荡]$|算了|晚了|迟了|来不及|够了|别[了问等想]$|不了|散[了掉场]|失去|回不去|落空|不值|不可能|没戏|没用|别靠近/.test(text)) return 'cold';
  // 梦感、模糊
  if (/梦[见里中]|月[亮光色]|星星|黄昏|晚霞|雾[里漫蒙]|影[子]|灯影|远处|像[是从真做在穿碰忘忽握落没记回]|好像[是在失坠飞漂]|总[在是像]|反复|凌晨|天[快将]亮/.test(text)) return 'dreamy';
  // 紧张、犹豫
  if (/[怕慌]|差一[点步]|不敢|犹豫|忍[住不]|退[了开后]|乱了|闷闷|发[紧酸沉]|撑不|稳不|喉咙|藏不住|咽回|堵|压了/.test(text)) return 'tense';
  // 安静、沉默
  if (/安静|发呆|轻轻|慢慢|窗[边外帘]|门[口边外]|台阶|角落|走廊|停[下了顿]/.test(text)) return 'quiet';
  return defaultMood;
}


// ─── 从池子构建 {text, mood} 数组 ─────────────────────────
function _buildCards(poolNames) {
  const cards = [];
  for (const name of poolNames) {
    const pool = _POOLS[name];
    if (!pool) continue;
    for (const text of pool.texts) {
      cards.push({ text, mood: _assignMood(text, pool.mood) });
    }
  }
  return cards;
}


