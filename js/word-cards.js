// ═══════════════════════════════════════════════════════════════
// WordCardEngine - 核心引擎
// ═══════════════════════════════════════════════════════════════

class WordCardEngine {
  constructor() {
    this.history = [];       // 所有轮次的记录
    this.usedTexts = new Set(); // 已抽过的卡文本（避免短期重复）
    this.customCards = [];   // 用户自定义卡片 [{text, id}]
    this.cardMode = "default"; // "default" | "custom" | "mixed"

    // 预处理：按 trigger 长度降序排列，保证最长匹配优先
    this._sortedTriggers = [];
    for (const kw of WORD_CARD_KEYWORDS) {
      for (const trigger of kw.triggers) {
        this._sortedTriggers.push({ trigger, keywordId: kw.id });
      }
    }
    this._sortedTriggers.sort((a, b) => b.trigger.length - a.trigger.length);

    // 快速查找 keyword by id
    this._keywordMap = {};
    for (const kw of WORD_CARD_KEYWORDS) {
      this._keywordMap[kw.id] = kw;
    }
  }

  // ─── 0. 自定义卡设置 ───────────────────────────────────
  setCustomCards(cards) {
    this.customCards = cards || [];
  }

  setCardMode(mode) {
    if (["default", "custom", "mixed"].includes(mode)) {
      this.cardMode = mode;
    }
  }

  // 从自定义卡池随机抽 n 张（去重）
  _drawCustom(n, tempUsed) {
    const available = this.customCards.filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
    const shuffled = available.sort(() => Math.random() - 0.5);
    const result = [];
    for (let i = 0; i < Math.min(n, shuffled.length); i++) {
      result.push({ text: shuffled[i].text, mood: "quiet", source: "custom" });
      tempUsed.add(shuffled[i].text);
    }
    return result;
  }

    // ─── 1. 关键词提取 ──────────────────────────────────────
  // 返回按在问句中出现位置排序的关键词 ID（去重，上限3）
  extractKeywords(question) {
    if (!question || !question.trim()) return [];

    // 去重复字符：hiii→hi, 啊啊啊→啊, 哈哈哈→哈哈
    const q = question.trim().replace(/(.)\1{2,}/g, '$1$1');
    const matched = []; // { id, position }
    const usedIds = new Set();
    const usedRanges = []; // 防止同一段文字被重复匹配

    for (const { trigger, keywordId } of this._sortedTriggers) {
      if (usedIds.has(keywordId)) continue;

      const pos = q.indexOf(trigger);
      if (pos === -1) continue;

      // 检查是否和已匹配的范围重叠
      const end = pos + trigger.length;
      const overlaps = usedRanges.some(([s, e]) =>
        (pos >= s && pos < e) || (end > s && end <= e)
      );
      if (overlaps) continue;

      matched.push({ id: keywordId, position: pos });
      usedIds.add(keywordId);
      usedRanges.push([pos, end]);

      if (matched.length >= 3) break;
    }

    // 按在问句中出现的位置排序
    matched.sort((a, b) => a.position - b.position);
    return matched.map(m => m.id);
  }

  // ─── 2. 加权随机抽卡 ────────────────────────────────────
  // 从卡池中根据 mood 亲和度加权抽取一张
  _drawFromPool(cards, preferMood) {
    // 过滤掉最近用过的
    let available = cards.filter(c => !this.usedTexts.has(c.text));
    if (available.length === 0) available = cards; // 全用过就重置

    if (!preferMood) {
      // 第一张卡：纯随机
      const card = available[Math.floor(Math.random() * available.length)];
      this.usedTexts.add(card.text);
      return card;
    }

    // 后续卡：mood 加权
    const weights = available.map(c => {
      return MOOD_AFFINITY[preferMood]?.[c.mood] || 1;
    });
    const totalWeight = weights.reduce((a, b) => a + b, 0);
    let roll = Math.random() * totalWeight;

    for (let i = 0; i < available.length; i++) {
      roll -= weights[i];
      if (roll <= 0) {
        this.usedTexts.add(available[i].text);
        return available[i];
      }
    }

    // fallback
    const card = available[available.length - 1];
    this.usedTexts.add(card.text);
    return card;
  }

  // ─── 3. 抽卡主函数 ──────────────────────────────────────
  // 输入问句，返回 { question, cards: [{text, mood}], keywordIds }
  // 最少 2 张卡，不足的用氛围池（场景/时间/身体/服装）补齐
  draw(question) {
    const keywordIds = this.extractKeywords(question);

    let cards = [];
    let currentMood = null;

    if (keywordIds.length === 0) {
      // 无匹配 → 通用池抽 1 张
      cards.push(this._drawFromPool(WORD_CARD_FALLBACK, null));
      currentMood = cards[0].mood;
    } else {
      for (const kwId of keywordIds) {
        const kw = this._keywordMap[kwId];
        if (!kw) continue;
        const card = this._drawFromPool(kw.cards, currentMood);
        cards.push(card);
        if (!currentMood) currentMood = card.mood; // 第一张定调
      }
    }

    // 氛围补卡：补到至少 2 张（问句 > 6 字补到 3 张）
    // 氛围 = 场景+时间碎片（在哪、什么时候）
    const minCards = (question && question.length > 6) ? 3 : 2;
    while (cards.length < minCards) {
      cards.push(this._drawFromPool(_ATMO, currentMood));
    }

    const round = {
      question: question || '',
      cards,
      keywordIds,
      timestamp: Date.now(),
    };
    this.history.push(round);

    // 控制 usedTexts 大小，超过 200 张清空一半（池子大了，提高阈值）
    if (this.usedTexts.size > 200) {
      const arr = [...this.usedTexts];
      this.usedTexts = new Set(arr.slice(arr.length / 2));
    }

    return round;
  }

  // ─── 4. 获取对话文本（保存/AI 用）──────────────────────
  getConversationText(maxChars) {
    const lines = [];
    for (const round of this.history) {
      if (round.question) lines.push(`问：${round.question}`);
      lines.push(`答：${round.cards.map(c => c.text).join(' · ')}`);
    }
    const text = lines.join('\n');
    if (maxChars && text.length > maxChars) {
      // 从后往前保留
      const recent = lines.slice(-Math.ceil(lines.length / 2)).join('\n');
      return recent.slice(-maxChars);
    }
    return text;
  }

  // ─── 5. 抽候选卡（给 AI 筛选用）─────────────────────────
  // 抽 n 张候选，不记入 history，由 AI 筛选后再 commit
  drawCandidates(question, n) {
    const keywordIds = this.extractKeywords(question);
    const candidates = [];
    const tempUsed = new Set();

    // ── 自定义卡模式：只抽自定义卡（没卡时回退到默认模式）──
    if (this.cardMode === "custom" && this.customCards.length > 0) {
      const custom = this._drawCustom(n, tempUsed);
      return { question, keywordIds: [], candidates: custom };
    }

    // ── 混合模式：系统卡 + 自定义卡各抽一半 ──
    if (this.cardMode === "mixed" && this.customCards.length > 0) {
      const halfSys = Math.ceil(n / 2);
      const halfCustom = n - halfSys;
      // 先抽系统关键词卡
      for (const kwId of keywordIds) {
        const kw = this._keywordMap[kwId];
        if (!kw) continue;
        const available = kw.cards.filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
        if (available.length === 0) continue;
        const count = Math.min(2, available.length);
        const shuffled = available.sort(() => Math.random() - 0.5);
        for (let i = 0; i < count && candidates.length < halfSys; i++) {
          candidates.push({ text: shuffled[i].text, mood: shuffled[i].mood, source: "keyword" });
          tempUsed.add(shuffled[i].text);
        }
      }
      // 再抽自定义卡
      const custom = this._drawCustom(Math.max(halfCustom, n - candidates.length), tempUsed);
      candidates.push(...custom);
      return { question, keywordIds, candidates };
    }

    // ── 默认模式：原有逻辑 ──
    // 从关键词池抽
    for (const kwId of keywordIds) {
      const kw = this._keywordMap[kwId];
      if (!kw) continue;
      const available = kw.cards.filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
      if (available.length === 0) continue;
      // 抽多张候选
      const count = Math.min(3, available.length);
      const shuffled = available.sort(() => Math.random() - 0.5);
      for (let i = 0; i < count; i++) {
        candidates.push({ text: shuffled[i].text, mood: shuffled[i].mood, source: 'keyword' });
        tempUsed.add(shuffled[i].text);
      }
    }

    // 从氛围池补充（仅当候选不足2张时，最多补1张）
    if (candidates.length < 2) {
      const atmoAvailable = _ATMO.filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
      const atmoShuffled = atmoAvailable.sort(() => Math.random() - 0.5);
      if (atmoShuffled.length > 0) {
        candidates.push({ text: atmoShuffled[0].text, mood: atmoShuffled[0].mood, source: 'atmo' });
      }
    }

    // ── 甜蜜补充：每次都塞 1-2 张亲密卡进候选 ──
    const sweetAvailable = _SWEET.filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
    const sweetShuffled = sweetAvailable.sort(() => Math.random() - 0.5);
    const sweetCount = Math.min(2, sweetShuffled.length);
    for (let i = 0; i < sweetCount; i++) {
      candidates.push({ text: sweetShuffled[i].text, mood: sweetShuffled[i].mood, source: 'sweet' });
      tempUsed.add(sweetShuffled[i].text);
    }

    return { question, keywordIds, candidates };
  }

  // ─── 6. 提交 AI 筛选结果 ──────────────────────────────
  // selectedTexts: AI 选中的卡片文本数组
  commitCards(question, keywordIds, selectedTexts) {
    const cards = selectedTexts.map(text => {
      // 找到原始 mood
      for (const kwId of keywordIds) {
        const kw = this._keywordMap[kwId];
        if (!kw) continue;
        const found = kw.cards.find(c => c.text === text);
        if (found) return { text: found.text, mood: found.mood };
      }
      const atmo = _ATMO.find(c => c.text === text);
      if (atmo) return { text: atmo.text, mood: atmo.mood };
      const fb = WORD_CARD_FALLBACK.find(c => c.text === text);
      if (fb) return { text: fb.text, mood: fb.mood };
      return { text, mood: 'quiet' };
    });

    for (const c of cards) this.usedTexts.add(c.text);

    const round = { question, cards, keywordIds, timestamp: Date.now() };
    this.history.push(round);

    if (this.usedTexts.size > 200) {
      const arr = [...this.usedTexts];
      this.usedTexts = new Set(arr.slice(arr.length / 2));
    }

    return round;
  }

  // ─── 7. 从指定池子名抽候选（AI 选池后调用）─────────────
  drawFromPoolNames(poolNames, n) {
    const candidates = [];
    const tempUsed = new Set();

    for (const name of poolNames) {
      const pool = _POOLS[name];
      if (!pool) continue;
      const cards = pool.texts
        .map(text => ({ text, mood: _assignMood(text, pool.mood) }))
        .filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
      const shuffled = cards.sort(() => Math.random() - 0.5);
      const count = Math.min(3, shuffled.length);
      for (let i = 0; i < count && candidates.length < n; i++) {
        candidates.push({ text: shuffled[i].text, mood: shuffled[i].mood, source: 'ai-pool' });
        tempUsed.add(shuffled[i].text);
      }
    }

    // 甜蜜补充
    const sweetAvailable = _SWEET.filter(c => !this.usedTexts.has(c.text) && !tempUsed.has(c.text));
    const sweetShuffled = sweetAvailable.sort(() => Math.random() - 0.5);
    const sweetCount = Math.min(2, sweetShuffled.length);
    for (let i = 0; i < sweetCount; i++) {
      candidates.push({ text: sweetShuffled[i].text, mood: sweetShuffled[i].mood, source: 'sweet' });
      tempUsed.add(sweetShuffled[i].text);
    }

    return { question: '', keywordIds: [], candidates };
  }

  // ─── 8. 获取所有池子名（前端用）──────────────────────────
  static getPoolNames() {
    return Object.keys(_POOLS);
  }

  // ─── 9. 重置 ───────────────────────────────────────────
  reset() {
    this.history = [];
    this.usedTexts.clear();
  }
}
