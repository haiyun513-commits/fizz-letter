class WordCardDeck {
  constructor() {
    this.rounds = [];      // 每轮 3 张卡
    this.maxRounds = 3;
  }

  // 根据用户输入判断意图
  detectIntent(question) {
    if (!question || !question.trim()) {
      // 无输入 → 随机
      const intents = Object.keys(WORD_POOLS);
      return intents[Math.floor(Math.random() * intents.length)];
    }
    const q = question.trim();
    let bestIntent = null;
    let bestScore = 0;
    for (const [intent, keywords] of Object.entries(INTENT_KEYWORDS)) {
      let score = 0;
      for (const kw of keywords) {
        if (q.includes(kw)) score++;
      }
      if (score > bestScore) {
        bestScore = score;
        bestIntent = intent;
      }
    }
    // 没匹配到 → 随机
    if (!bestIntent) {
      const intents = Object.keys(WORD_POOLS);
      return intents[Math.floor(Math.random() * intents.length)];
    }
    return bestIntent;
  }

  // 从槽位随机抽 1 个，避免与已用的重复
  _pickFromSlot(slot, used) {
    const available = slot.filter(w => !used.has(w));
    if (available.length === 0) return slot[Math.floor(Math.random() * slot.length)];
    return available[Math.floor(Math.random() * available.length)];
  }

  // 抽 3 张卡
  draw(question) {
    const intent = this.detectIntent(question);
    const pool = WORD_POOLS[intent];

    // 收集已用过的词
    const used = new Set();
    for (const round of this.rounds) {
      round.cards.forEach(c => used.add(c.text));
    }

    const cards = [
      { text: this._pickFromSlot(pool.slot1, used), slot: 'scene' },
      { text: this._pickFromSlot(pool.slot2, used), slot: 'action' },
      { text: this._pickFromSlot(pool.slot3, used), slot: 'taste' },
    ];

    const round = { intent, label: pool.label, cards, timestamp: Date.now() };
    this.rounds.push(round);
    return round;
  }

  canDrawMore() {
    return this.rounds.length < this.maxRounds;
  }

  reset() {
    this.rounds = [];
  }

  // 获取全部文本（用于存档）
  getAllText() {
    return this.rounds.map(r =>
      r.cards.map(c => c.text).join(' · ')
    ).join('\n');
  }
}
