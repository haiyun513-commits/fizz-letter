function matchCategory(question) {
  if (!question || !question.trim()) return null;
  const q = question.trim();
  let bestMatch = null;
  let bestScore = 0;
  for (const [cat, data] of Object.entries(ANSWER_CATEGORIES)) {
    for (const kw of data.keywords) {
      if (q.includes(kw)) {
        const score = kw.length;
        if (score > bestScore) {
          bestScore = score;
          bestMatch = cat;
        }
      }
    }
  }
  return bestMatch;
}

class AnswerBook {
  constructor() {
    this.currentWord = '';
    this.currentResponse = '';
  }

  pickRandomWord(question) {
    const cat = matchCategory(question);
    const pool = cat ? ANSWER_CATEGORIES[cat].pool : ANSWER_POOL_FALLBACK;
    return pool[Math.floor(Math.random() * pool.length)];
  }

  async getAnswer(question) {
    this.currentWord = this.pickRandomWord(question);
    const res = await fetch('/api/answer', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ question: question || '', word: this.currentWord }),
    });
    if (!res.ok) throw new Error('API failed');
    const data = await res.json();
    if (data.error) throw new Error(data.error);
    this.currentResponse = data.response;
    return { word: this.currentWord, response: this.currentResponse };
  }

  getFallbackAnswer(question) {
    this.currentWord = this.pickRandomWord(question);
    this.currentResponse = '';
    return { word: this.currentWord, response: this.currentResponse };
  }
}
