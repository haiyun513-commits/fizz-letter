// 根据选中的词匹配风格
function detectStyle(selectedWords) {
  const scores = {};
  BUBBLE_GROUPS.forEach(group => {
    scores[group.id] = 0;
    selectedWords.forEach(word => {
      if (group.words.includes(word)) {
        scores[group.id]++;
      }
    });
  });
  let maxScore = 0;
  let bestStyle = 'atmosphere';
  for (const [style, score] of Object.entries(scores)) {
    if (score > maxScore) {
      maxScore = score;
      bestStyle = style;
    }
  }
  return bestStyle;
}

// 获取信纸模板
function getLetterTemplate(style) {
  return STYLE_MAP[style] || 'modern';
}

// 随机获取一封信（fallback用 - 从所有预写信里随机取一封）
function getRandomLetter(style, userMessage) {
  const allLetters = Object.values(LETTERS).flat();
  const pool = allLetters.length > 0 ? allLetters : LETTERS.fairy;
  const letter = pool[Math.floor(Math.random() * pool.length)];
  
  let body = letter.body;
  if (userMessage && userMessage.trim().length > 0) {
    const responses = [
      '你写的那些话，我一个字一个字地读了。\n\n',
      '谢谢你愿意告诉我。光是说出来，就已经很勇敢了。\n\n',
      '我收到了。每一个字都收到了。\n\n',
      '你的话落在这里了。像一颗石子落进安静的湖。涟漪很轻，但我都看到了。\n\n',
    ];
    body = responses[Math.floor(Math.random() * responses.length)] + body;
  }
  
  return {
    body,
    closing: letter.closing,
    template: getLetterTemplate(style),
  };
}
